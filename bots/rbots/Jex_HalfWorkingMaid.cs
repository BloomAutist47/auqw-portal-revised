	public void ExitCombat()
	{
		bot.Player.Jump("Wait", "Spawn");
		while (bot.Player.State == 2) { }
	}


	public void SafeMapJoin(string MapName, string CellName = "Enter", string PadName = "Spawn",string roomNumber ="999999")
	{
		string mapname = MapName.ToLower();
		while (bot.Map.Name != mapname)
		{
			ExitCombat();
			if (mapname == "tercessuinotlim") bot.Player.Jump("m22", "Center");
			bot.Player.Join($"{mapname}-{roomNumber}", CellName, PadName);
			bot.Wait.ForMapLoad(mapname);
			bot.Sleep(500);
		}
		if (bot.Player.Cell != CellName) bot.Player.Jump(CellName, PadName);
	}

    public void FollowPlayer(string playerName){
        Stuff:
            bot.SendPacket($"%xt%zm%cmd%1%goto%{playerName}%");
            while (!bot.Map.Loaded) { };
            while (!bot.Player.Loaded) { };
            bot.Sleep(500); 
            if(bot.Map.GetPlayer(playerName) == null) {
                bot.Sleep(6000);
                if(Counter == 6) {bot.Player.Logout();ScriptManager.StopScript();}
                Counter++;
                goto Stuff;
            }else {bot.Player.Goto(playerName);}
    }



